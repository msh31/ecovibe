<script>
    // Mobile menu toggle
    document.querySelector('.md\\:hidden').addEventListener('click', function() {
        document.querySelector('.mobile-menu').classList.toggle('hidden');
    });

    // Cart popup functionality
    function showCartPopup() {
        const popup = document.getElementById('cart-popup');
        popup.classList.remove('hidden');
        popup.classList.add('animate__animated', 'animate__fadeInRight');

        setTimeout(() => {
            popup.classList.remove('animate__fadeInRight');
            popup.classList.add('animate__fadeOutRight');
            setTimeout(() => {
                popup.classList.add('hidden');
                popup.classList.remove('animate__fadeOutRight');
            }, 500);
        }, 3000);
    }

    // Add click event to all "Add to cart" buttons
    document.querySelectorAll('button').forEach(button => {
        if (button.textContent.trim() === 'In winkelwagen') {
            button.addEventListener('click', showCartPopup);
        }
    });
</script>

